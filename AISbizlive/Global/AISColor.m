#import "AISColor.h"

@implementation AISColor

+(UIColor *)greenColor{
    return [UIColor colorWithRed:255.0/255.0 green:255.0/255.0 blue:255.0/255.0 alpha:1.0];
}
+(UIColor *)lightgreenColor{
    return [UIColor colorWithRed:191.0/255.0 green:210.0/255.0 blue:43.0/255.0 alpha:1.0];
}
+(UIColor *)grayColor{
    return [UIColor colorWithRed:70.0/255.0 green:55.0/255.0 blue:56.0/255.0 alpha:1.0];
}
+(UIColor *)lightgrayColor{
    return [UIColor colorWithRed:191.0/255.0 green:191.0/255.0 blue:191.0/255.0 alpha:1.0];
}
+(UIColor *)redColor{
    return [UIColor redColor];
}
@end
